function getFilteredProfiles() {
  const query = normalize(els.query.value);
  const city = normalize(els.city.value);
  const specialty = normalize(els.specialty.value);
  const clientType = normalize(els.clientType.value);

  const filtered = profiles.filter((profile) => {
    const blob = normalize(getSearchBlob(profile));
    const matchesQuery = !query || blob.includes(query);
    const matchesCity = !city || normalize(profile.city) === city;
    const matchesSpecialty =
      !specialty || (profile.specialties || []).some((item) => normalize(item) === specialty);
    const matchesClient =
      !clientType || (profile.clients || []).some((item) => normalize(item) === clientType);
    const matchesAccepting = !els.acceptingOnly.checked || profile.accepting;
    const matchesVirtual = !els.virtualOnly.checked || profile.virtual;
    const matchesVerified = !els.verifiedOnly.checked || profile.verified;

    return (
      matchesQuery &&
      matchesCity &&
      matchesSpecialty &&
      matchesClient &&
      matchesAccepting &&
      matchesVirtual &&
      matchesVerified
    );
  });

  return sortProfiles(filtered);
}

function sortProfiles(items) {
  const sorted = [...items];

  if (els.sortBy.value === "rating") {
    sorted.sort((a, b) => Number(b.rating || 0) - Number(a.rating || 0));
  } else if (els.sortBy.value === "experience") {
    sorted.sort((a, b) => Number(b.experience || 0) - Number(a.experience || 0));
  } else if (els.sortBy.value === "fee") {
    sorted.sort((a, b) => Number(a.fee || 0) - Number(b.fee || 0));
  } else {
    sorted.sort((a, b) => {
      const scoreA = Number(a.verified) * 20 + Number(a.accepting) * 10 + Number(a.rating || 0);
      const scoreB = Number(b.verified) * 20 + Number(b.accepting) * 10 + Number(b.rating || 0);
      return scoreB - scoreA;
    });
  }

  return sorted;
}

