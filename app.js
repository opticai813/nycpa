async function submitChat(event) {
  event.preventDefault();
  const question = els.chatInput.value.trim();
  if (!question) return;

  chatState.messages.push({ role: "user", content: question });
  appendChatMessage("user", "You", question);
  els.chatInput.value = "";
  setChatLoading(true);
  const loadingEl = appendChatMessage("assistant loading", "NYCPA AI", "Thinking...");

  try {
    const response = await fetch("/api/chat", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ messages: chatState.messages })
    });
    const data = await response.json().catch(() => ({}));

    if (!response.ok) {
      throw new Error(data.error || "The AI chat is not available right now.");
    }

    const answer = data.answer || "I could not generate an answer. Please try again.";
    chatState.messages.push({ role: "assistant", content: answer });
    loadingEl.remove();
    appendChatMessage("assistant", "NYCPA AI", answer);
  } catch (error) {
    loadingEl.remove();
    appendChatMessage(
      "assistant",
      "NYCPA AI",
      `${error.message}\n\nOn GitHub Pages, the CPA directory works as a static site, but AI chat needs a live backend API. For local testing, start the server with npm start and open http://127.0.0.1:8787.`
    );
  } finally {
    setChatLoading(false);
  }
}

function appendChatMessage(type, label, text) {
  const message = document.createElement("div");
  message.className = `chat-message ${type}`;
  message.innerHTML = `
    <strong>${escapeHtml(label)}</strong>
    <p>${escapeHtml(text)}</p>
  `;
  els.chatLog.append(message);
  els.chatLog.scrollTop = els.chatLog.scrollHeight;
  return message;
}

function setChatLoading(isLoading) {
  els.chatSubmit.disabled = isLoading;
  els.chatSubmit.textContent = isLoading ? "Asking..." : "Ask";
}

function csvCell(value) {
  const text = String(value ?? "");
  return `"${text.replaceAll('"', '""')}"`;
}

function escapeHtml(value) {
  return String(value ?? "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function escapeAttribute(value) {
  return escapeHtml(value).replaceAll("`", "&#096;");
}

